class ZipRef extends PkRefManager {
  public static $milesperlat = 69;
  public static $milesperlong = 57;

  /**
   * OLD very primative estimates. See below for better implementations
   * @param null $withinmiles
   * @param int $ofzip
   * @return typeReturns rows of California zips, optionally within miles of zip
   * @param int|null $withinmiles - if null, all zips. If not null, $ofzip must also be specified
   * @param int|null $ofzip - if $withinmiles is specified, $ofzip must also be, and exist in the table
   */
  public static function getCaliforniaZips($withinmiles = null, $ofzip = null) {
    if ($withinmiles === null) return static::$ca_cities;
    if (!$ofzip) return null; #Invalid params
    $row = false;
    foreach (static::$ca_cities as $ca_city) {
      if ($ca_city[0] == $ofzip) {
        $row = $ca_city;
        break;
      }
    }
    if (!$row) return null; #couldn't find zip
    $lat = $row[3];
    $long = $row[4];
    $retarr = [];
    foreach (static::$ca_cities as $ca_city) {
      if (  ((static::$milesperlat * abs($ca_city[3] - $lat)) < $withinmiles)
        && ((static::$milesperlong * abs($ca_city[4] - $long)) < $withinmiles)) {
        $retarr[] = $ca_city;
      }
    }
    return $retarr;
  }

  /** Returns an ASSOCIATIVE array of locations
   * 
   * @param type $withinmiles
   * @param type $ofzip
   */
  public static function getCaliforniaZipsAssoc($withinmiles = null, $ofzip = null) {
    $locations = static::getCaliforniaZips($withinmiles, $ofzip);
    $assoclocs = [];
    foreach ($locations as $location) {
      $assoclocs[] = static::locationAssoc($location);
    }
    return $assoclocs;
  }

  /** Converts postal code to lat/lng
   * @param string $zip - postal code
   * @param string $cc - country code - default US
   * @return float array lat/lng or null
  */
  public static function zipToGeo(string|int $zip, string $cc='us') : array|null {
    $cc = strtolower($cc);
    if ($cc === 'us') { // Just some California zips for now
      $fUsCities = GeoData::getUsCities();
      //foreach ($ca_cities as $row) {

      //foreach ($US_cities as $row) {
      //foreach ($reqUsCities as $row) {
      foreach ($fUsCities as $row) {
        if ($row[0] == $zip) {
          $ziprow = $row;
          break;
        }
      }
      if (!$ziprow) {
        pkwarn("Zip [$zip] not found for country [$cc]");
        return null;
      }
      $point = [$ziprow[3], $ziprow[4]];
      return $point;
    }
    pkwarn("Contry code [$cc] not supported");
    return null;
  }

  /**
   * 
   */
  public static function distBetweenZipsMt($zip1, $zip2, $cc='us') {
    $pt1 = static::zipToGeo($zip1, $cc);
    $pt2 = static::zipToGeo($zip2, $cc);
    $dist = geoDistMt($pt1, $pt2);
    return $dist;
  }



  /**Takes a single index location (ca_cities) row & returns as a meaningful
   * associative array of zip, city, state, latitude, longitude
   */
  public static function locationAssoc($location) {
      return ['zip'=>$location[0], 'state'=>$location[1], 'city'=>$location[2],
          'latitude'=>$location[3], 'longitude'=>$location[4]];
  }



  public static function randLocation($items = -1) {
    $rawLocations = PkTestGenerator::randData(static::$ca_cities, $items);
    if ($items === -1) return static::locationAssoc($rawLocations);
    $assocLocs = [];
    foreach ($rawLocations as $rawLocation) {
      $assocLocs[] = static::locationAssoc($rawLocation);
    }
    return $assocLocs;
  }

